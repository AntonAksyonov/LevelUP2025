package HomeWork4;

public class Calculator {
    public static int addition(int summandA, int summandB) {
        return (summandA + summandB);
    }

    public static long addition(long summandA, long summandB) {
        return (summandA + summandB);
    }

    public static double addition(double summandA, double summandB) {
        return (summandA + summandB);
    }

    public static int subtraction(int reduced, int subtracted) {
        return (reduced - subtracted);
    }

    public static long subtraction(long reduced, long subtracted) {
        return (reduced - subtracted);
    }

    public static double subtraction(double reduced, double subtracted) {
        return (reduced - subtracted);
    }

    public static int multiplication(int multiplierA, int multiplierB) {
        return (multiplierA * multiplierB);
    }

    public static long multiplication(long multiplierA, long multiplierB) {
        return (multiplierA * multiplierB);
    }

    public static double multiplication(double multiplierA, double multiplierB) {
        return (multiplierA * multiplierB);
    }

    public static int separation(int divisible, int divisor) {
        return (divisible / divisor);
    }

    public static long separation(long divisible, long divisor) {
        return (divisible / divisor);
    }

    public static double separation(double divisible, double divisor) {
        return (divisible / divisor);
    }

}
